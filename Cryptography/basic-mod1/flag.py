lst = [350, 63, 353, 198, 114, 369, 346, 184, 202, 322, 94, 235, 114, 110, 185, 188, 225, 212, 366, 374, 261, 213]

s = ''

for i in lst:
    if i % 37 >= 0 and i % 37 <= 25:
        s += chr(i % 37 + ord('A'))
    elif i % 37 >= 26 and i % 37 <= 35:
        s += str(i % 37 - 26)
    else:
       s += '_'

print(s)